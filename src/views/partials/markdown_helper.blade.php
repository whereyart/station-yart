<p>
  We use the <a href="http://en.wikipedia.org/wiki/Markdown" target="_blank">Markdown</a> syntax to format text. Here is a <a href="https://github.com/adam-p/markdown-here/wiki/Markdown-Cheatsheet" target="_blank">full reference</a>. <br />
  You can <a target="_blank" href="https://www.google.com/search?q=markdown">google 'markdown'</a> for more help.
</p>

<h7>Headers</h7>
<pre>
# Level 1 Header (H1)
## Level 2 Header (H2)
##### Level 5 Header (H5)
</pre>

<h7>Paragraphs</h7>
<pre>
One or more consecutive lines of text separated by one or more blank lines. Don't use line breaks unless you really need to.
 
This is another paragraph.

I am a sentence with  
a line break.
</pre>

<h7>Lists</h7>
<pre>
Unordered:

* Red
* Green
* Blue

+ Red
+ Green
+ Blue

- Red
- Green
- Blue

Ordered / Numbered:

1. Bird
2. McHale
3. Parish
</pre>

<h7>Emphasis</h7>

<pre>
I am **bold**
I am __bold__
I am *italicized*
I am _italicized_
</pre>

<h7>Links</h7>

<pre>
This is [an example](http://example.com/ "Optional Title") 
inline link.

or make automatic links:

&lt;http://example.com/&gt;
&lt;address@example.com&gt;
</pre>

<h7>Images</h7>

<pre>
![Alt text](/link/to/image.jpg "Text Title")
</pre>

<h7>Tables</h7>
<pre>
| First Header    |   Second Header |
| --------------- | --------------- |
| Row 1 Cell 1    | Row 1 Cell 2    |
| Row 2 Cell 1    | Row 2 Cell 2    |
</pre>

<p>There is much more you can do! <a href="https://github.com/adam-p/markdown-here/wiki/Markdown-Cheatsheet" target="_blank">Read more</a>.</p>

